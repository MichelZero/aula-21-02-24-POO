def absoluto(numero):
  """
  Retorna o valor absoluto de um número.

  Argumentos:
    numero: O número inteiro a ser processado.

  Retorno:
    O valor absoluto de `numero`.
  """
  # Implemente a função
  
  if numero > 0:
    return numero
  else:
    return -numero